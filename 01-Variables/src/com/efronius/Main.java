package com.efronius;

public class Main {

    public static void main(String[] args) {
	// Variables pada java : String, int, float, char dan boolean

        //String tipe
        String name = "John";
        System.out.println("My Name is : "+name);

        //Int tipe
        int age = 22;
        System.out.println("I am " + age + " " + "years old.");

        //float tipe
        float ipk = 3.99f;
        System.out.println("My index value : "+ipk);

        //char tipe
        char grade = 'A';
        System.out.println("Grade : "+grade);

        //boolean tipe
        boolean status = true;
        System.out.println("Status : "+status);

    }
}
