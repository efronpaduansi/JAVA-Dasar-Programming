package com.efronius;

public class Main {

    public static void main(String[] args) {
	// kita akan bisa bermain dengan matematika

        //Nilai Max
        System.out.println(Math.max(100, 1000));

        //Nilai Min.
        System.out.println(Math.min(10, 100));

        //Nilai akar
        System.out.println(Math.sqrt(81));

        //Nilai mutlak / abs
        System.out.println(Math.abs(-29));

        System.out.println(Math.random());


    }
}
